bool process_record_user(uint16_t keycode, keyrecord_t *record) {
  switch (keycode) {
    case QWERTY:
      if (record->event.pressed) {
        set_single_persistent_default_layer(_QWERTY);
      }
      return false;
    case CUSTOM:
      if (record->event.pressed) {
        layer_on(_CUSTOM);
        update_tri_layer(_CUSTOM, _QWERTY, _ADJUST);
      } else {
        layer_off(_CUSTOM);
        update_tri_layer(_CUSTOM, _QWERTY, _ADJUST);
      }
      return false;
  }
  return true;
}

void encoder_update_user(uint8_t index, bool clockwise) {
    if (index == 0) {
        if (clockwise) {
            tap_code(KC_VOLU);
        } else {
            tap_code(KC_VOLD);
        }
    }
    else if (index == 1) {
        if (clockwise) {
            tap_code(KC_PGDN);
        } else {
            tap_code(KC_PGUP);
        }
    }
}
